namespace Bss\ConfiguableGridView\Model\Config\Source;

class UnitPrice implements \Magento\Framework\Option\ArrayInterface
{
    /**
     * Options getter
     *
     * @return array
     */
    public function toOptionArray()
    {
        return [['value' => 1, 'label' => __('Yes')], ['value' => 0, 'label' => __('No')], ['value' => 2, 'label' => __('Only Different Price')]];
    }

    /**
     * Get options in "key-value" format
     *
     * @return array
     */
    public function toArray()
    {
        return [0 => __('No'), 1 => __('Yes'), 2 => __('Only Different Price')];
    }
}
