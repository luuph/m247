namespace Bss\CustomOptionAbsolutePriceQuantity\Controller\Adminhtml\Stock;

use Magento\Backend\App\Action\Context;
use Magento\Framework\View\Result\PageFactory;

class Index extends \Magento\Backend\App\Action
{
    /**
     * @var PageFactory
     */
    protected $resultPageFactory;

    /**
     * @param Context $context
     * @param PageFactory $resultPageFactory
     */
    public function __construct(
        Context $context,
        PageFactory $resultPageFactory
    ) {
        parent::__construct($context);
        $this->resultPageFactory = $resultPageFactory;
    }

    /**
     * Index action
     *
     * @return \Magento\Backend\Model\View\Result\Page
     */
    public function execute()
    {
        /** @var \Magento\Backend\Model\View\Result\Page $resultPage */
        $resultPage = $this->resultPageFactory->create();
        $resultPage->setActiveMenu('Bss_CustomOptionAbsolutePriceQuantity::bss_custom_option_stock');
        $resultPage->addBreadcrumb(__('Manage Stock Option Change'), __('Manage Stock Option Change'));
        $resultPage->getConfig()->getTitle()->prepend(__('Manage Stock Option Change'));

        return $resultPage;
    }

    /**
     * @return bool
     */
    protected function _isAllowed()
    {
        return $this->_authorization->isAllowed('Bss_CustomOptionAbsolutePriceQuantity::bss_custom_option_stock');
    }
}
