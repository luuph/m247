namespace Bss\Gallery\Block\Adminhtml\Category\Helper;

use Magento\Framework\Data\Form\Element\Image as ImageField;
use Magento\Framework\Data\Form\Element\Factory as ElementFactory;
use Magento\Framework\Data\Form\Element\CollectionFactory as ElementCollectionFactory;
use Magento\Framework\Escaper;
use Bss\Gallery\Model\Category\Image as CategoryImage;
use Magento\Framework\UrlInterface;

/**
 * Class Image
 *
 * @package Bss\Gallery\Block\Adminhtml\Category\Helper
 */
class Image extends ImageField
{
    /**
     * image model
     *
     * @var \Bss\Gallery\Model\Category\Image
     */
    protected $imageModel;

    /**
     * Image constructor.
     *
     * @param CategoryImage $imageModel
     * @param ElementFactory $factoryElement
     * @param ElementCollectionFactory $factoryCollection
     * @param Escaper $escaper
     * @param UrlInterface $urlBuilder
     * @param array $data
     */
    public function __construct(
        CategoryImage $imageModel,
        ElementFactory $factoryElement,
        ElementCollectionFactory $factoryCollection,
        Escaper $escaper,
        UrlInterface $urlBuilder,
        $data = []
    ) {
        $this->imageModel = $imageModel;
        parent::__construct($factoryElement, $factoryCollection, $escaper, $urlBuilder, $data);
    }

    /**
     * Get image preview url
     *
     * @return string
     */
    protected function _getUrl()
    {
        $url = false;
        if ($this->getValue()) {
            $url = $this->imageModel->getBaseUrl() . $this->getValue();
        }
        return $url;
    }
}
